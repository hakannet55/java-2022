package functions;

public class DortIslem {
    public int Topla(int say1, int say2) {
        return say1 + say2;
    }

    public int Cikar(int say1, int say2) {
        return say1 - say2;
    }

    public int Carpma(int say1, int say2) {
        return say1 * say2;
    }

    public int Bolme(int say1, int say2) {
        return say1 / say2;
    }
}
